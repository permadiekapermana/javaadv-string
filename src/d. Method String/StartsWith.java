public class StartsWith {
    public static void main(String[] args) {
        String str1 = "Javascript";
        // String pre fix, memeriksa apakah string tersebut dituliskan dengan huruf dimulai dari ...
        // output berupa boolean
        System.out.println(str1.startsWith("J"));
        System.out.println(str1.startsWith("P"));
    }
}
